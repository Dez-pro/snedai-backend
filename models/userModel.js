import pkg from 'mongoose';
const { Schema, models, model } = pkg;

const UserSchema = new Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      enum: ['citoyen', 'chercheur', 'décideur', 'autorité'],
      default: 'citoyen',
    },
    city: {
      type: String,
      required: true,
    },
    resetPasswordToken: {
      type: String,
      default: null,
    },
    resetPasswordExpires: {
      type: Date,
      default: null,
    },
  },
  {
    timestamps: true,
  }
);

// Export du modèle
export default models.User || model('User', UserSchema);